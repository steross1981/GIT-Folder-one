    private void CreateFromExistingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateFromExistingActionPerformed
        try {
            // TODO add your handling code here:
            String custEmail = CustomerEmailCombo.getSelectedItem().toString();
            String evtttType = evTypeField.getText();
            String evtttName = eventNameField.getText();
            String enttttName = chooseEntNameCombo.getSelectedItem().toString();
            java.util.Date d;

            cal = dateChooserPanel1.getSelectedDate();
            d = cal.getTime();
            java.sql.Date sd = new java.sql.Date(d.getTime());
            date = sd;
            EventConnection ec = new EventConnection("coursework");
            ResultSet rs = ec.getDatesOfEntertainerEngagement(enttttName, date);

            if (!rs.next()) {
                Event evl = new Event(evtttType, sd, evtttName);
                ec.addEventFromExistingCustomer(evl, enttttName, custEmail);
                ec.closeConnection();
            } else {
                JOptionPane.showMessageDialog(null, "Entertainer is Already Booked on This Date.\nPlease Choose Another Date");
            }
        } catch (SQLException ex) {
            Logger.getLogger(EngagementFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_CreateFromExistingActionPerformed

