    private void addNewEntertainerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNewEntertainerButtonActionPerformed
        try {
            // TODO add your handling code here:
            String name = entertainerNameField.getText();
            EntertainerConnection eC = new EntertainerConnection("coursework");
            ResultSet rs = eC.checkEntertainerExist(name);
            entertainerNameField.setText(null);

            if (!rs.next()) {
                try {

                    String type = entertainerTypeField.getText();
                    entertainerTypeField.setText(null);
                    String f = entertainerFeeField.getText();
                    int fee = Integer.parseInt(f);
                    entertainerFeeField.setText(null);
                    Entertainer newEnt = new Entertainer(name, type, fee);

                    eC.addEntertainer(newEnt);
                    JOptionPane.showMessageDialog(null, "Success");
                    eC.showEntertainersNames(EntNameTable);
                    chooseEntNameCombo.removeAllItems();
                    eC.getAllnames(chooseEntNameCombo);

                } catch (SQLException ex) {
                    Logger.getLogger(EngagementFrame.class.getName()).log(Level.SEVERE, null, ex);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Please Enter a Numeric Value for Fee" + e.toString());
                }
                eC.closeConnection();
            } else {
                JOptionPane.showMessageDialog(null, "Entertainer Already Exists in Database");
            }
        } catch (SQLException ex) {
            Logger.getLogger(EngagementFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addNewEntertainerButtonActionPerformed

